struct Cheater
{
    uint IP;
};
vector <Cheater> Cheaters;

void AddPban (uint ip)
{
    loopv (Cheaters)
    {
        if (Cheaters [i].IP == ip) return;
    }
    Cheater & Pban = Cheaters.add ();
    Pban.IP = ip;
}

ICOMMAND (pban, "i", (uint * IP), AddPban (* IP));

